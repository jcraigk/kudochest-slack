class ClaimPolicy
  attr_reader :user, :claim

  def initialize(user, claim)
    @user = user
    @claim = claim
  end

  def index?
    user.owned_team.present?
  end

  def edit?
    user_owns_team?
  end

  def update?
    user_owns_team?
  end

  def destroy?
    claim.fulfilled_at.blank? && (user_owns_team? || user_owns_claim?)
  end

  def show?
    user_owns_claim? || user_owns_team?
  end

  private

  def user_owns_team?
    claim.reward.team.owning_user == user
  end

  def user_owns_claim?
    claim.profile_id == user.profile.id
  end
end
